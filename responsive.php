        <h1>Responsive</h1>
    </header>
    <p>
        le Responsive Web Design (RWD) est une technique de conception d'interface digitale qui fait en sorte que l'affichage d'une quelconque page 
        d'un site s'adapte de façon automatique à la taille de l'écran du terminal qui le lit.
    </p>
    <h3><strong>Comment</strong> créer un site web responsive ?</h3>
    <p>
        Le principe est simple, il suffit principalement d'utiliser les media queries pour les css : en clair, cela permet d'interroger le navigateur pour savoir quelle est 
        la taille en pixels de la zone d'affichage, le type d'écran, la position, puis de déterminer les paramètres de l'affichage en fonction de ces résultats : 
        par exemple, le bloc B est à gauche du bloc A si l'affichage sur l'écran est supérieur à 1024 pixels, mais il passe en dessous dans le cas contraire. 
        Mais le bloc C peut aussi disparaître parce que la fonction concernée n'est pas jugée utile sur les mobiles, le bloc D peut devenir D' avec de nouveaux contenus...etc. 
        En pratique, les mises en page fluides requièrent une bonne connaissance du CSS 3 et le code d'un ancien site doit parfois être entièrement revu pour que 
        l'affichage soit optimal pour tous les écrans. Le repositionnement fluide des éléments du site, et leur personnalisation au media utilisé ont fait 
        de l'optimisation multi-supports un nouveau besoin, en tout cas par son ampleur.
    </p>
    <h3>Peut-on <strong>rendre "responsive"</strong> un site existant ?</h3>
    <p>
        Tout dépend du site. Ce qui peut être une simple adaptation réalisée en quelques heures peut aussi être un vrai casse-tête pour des sites plus anciens, 
        selon la technologie utilisée, le langage, le design. Il faut dans tous les cas faire une étude pour voir la solution technique la plus appropriée : 
        refonte graphique complète, réintégration dans un CMS, utilisation d'un template, simple adaptation des css, ... de multiples cas de figure sont possibles.
    </p>
    <h3><strong>Combien</strong> coûte une adaptation responsive ?</h3>
    <p>
        L'éventail est très large. Lorsque vous concevez un nouveau site, vous pouvez utiliser un template existant déjà conforme aux exigences de responsive 
        web design et le personnaliser pour quelques dizaines d'euros. Pour un site beaucoup plus ancien et très complexe, avec un nombre important de pages et 
        de modules, la personnalisation responsive peut prendre plusieurs jours. Dans tous les cas, un rapide audit de votre site vous permettra d'avoir un chiffrage 
        précis et d'évaluer la faisabilité d'une transition efficace.
    </p>
